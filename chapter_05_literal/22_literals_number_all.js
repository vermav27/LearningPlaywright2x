// Integer literal
let age = 25;
console.log(typeof age); // number

// Hex Integer literal
let hexValue = 0x1F;
console.log(typeof hexValue); // number

// Binary literal
let binaryValue = 0b1010;
console.log(typeof binaryValue); // number

// Octal literal
let octalValue = 0o17;
console.log(typeof octalValue); // number

// Float literal
let price = 10.5;
console.log(typeof price); // number

// Scientific Number literal
let distance = 1e3;
console.log(typeof distance); // number

