// null literal
// null means there is no value intentionally.
let emptyValue = null;

// undefined literal
// undefined means a value has not been given yet.
let notAssigned;

// Example
console.log(emptyValue);      // null
console.log(notAssigned);     // undefined

// Layman explanation
// null = "I know there is no value."
// undefined = "I have not set a value yet."
